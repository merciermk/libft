#include "libft.h"

char *ft_strdup(const char *s1)
{
	char *s2;
	s2 = malloc (sizeof (*s1) * ft_strlen (s1) +1);
	if (s2)
	{
		ft_strcpy (s2, s1);
		return (s2);
	}
	return (0);
}

