#include "libft.h"

char *ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s) + 1;
	while (s[i - 1] != (char)c && i > 0)
		i--;
	if (i > 0)
		return ((char*)s + i - 1);
	return (NULL);
}
